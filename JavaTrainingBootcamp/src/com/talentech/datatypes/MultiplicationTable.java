public class MultiplicationTable {

	public static void main(String[] args) {
		
		// Change here to change output 
        int n = 5;  
          
        for (int i = 1; i <= 10; ++i)  
            System.out.println(n + " * " + i + 
                               " = " + n * i); 
	}


}
